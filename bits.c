/* 
 * float_abs - Return bit-level equivalent of absolute value of f for
 *    floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representations of
 *   single-precision floating point values.
 *   When argument is NaN, return argument..
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 10
 *   Rating: 2
 */
unsigned float_abs(unsigned uf) {
  unsigned m = 0x7FFFFFFF;
  unsigned abs = m & uf;
  unsigned nan = 0x7F800001;
  if (abs >= nan) {
    return uf;
  } else {
    return abs;
  }
}
/* 
 * float_half - Return bit-level equivalent of expression 0.5*f for
 *   floating point argument f.
 *   Both the argument and result are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_half(unsigned uf) {
   unsigned exp = (uf & 0x7F800000) >> 23;
   unsigned mantissa = uf & 0x7FFFFF;
   unsigned sign = uf >> 31;
   unsigned half, halfexp;
   if (!(~(exp)& 0xff)) {
     return uf;
   } else if (uf == 0) {
     return 0;
   } else if (uf == 0x800000) {
     return 0x400000;
   } else {
     halfexp = (exp + 0xff) & 0xff;
     half = (sign << 31) | (halfexp << 23) | mantissa;
     return half;
   }
}
/* 
 * float_f2i - Return bit-level equivalent of expression (int) f
 *   for floating point argument f.
 *   Argument is passed as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point value.
 *   Anything out of range (including NaN and infinity) should return
 *   0x80000000u.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
int float_f2i(unsigned uf) {
  int exp = (uf >> 23) & 0xFF; // shift right by # of bits for mantissa to get exponent
  int frac = uf & 0x7FFFFF; // get mantissa by masking the rightmost 23 bits
  // case when uf is infinity
  if (exp == 0x7F800000) {
    return 0x80000000u;
  }
  // case when exponent after correction for bias is negative
  // rounds down to 0 
  if (exp < 127) {
    return 0;
  }
  // case when uf is nan
  if (exp > 157) {
    return 0x80000000u;
  }
  // normalized, set leftmost bit of mantissa to 1
  frac = frac | 0x800000;
  //  
  if (exp >= 150) {
    frac = frac << (exp - 150);
  } else {
    frac = frac >> (150 - exp);
  }
  // if float is negative, return two's complement of mantissa
  if (uf < 0) {
    return -1*frac;
  }
 
  return frac;
}
