/* 
 * subOK - Determine if can compute x-y without overflow
 *   Example: subOK(0x80000000,0x80000000) = 1,
 *            subOK(0x80000000,0x70000000) = 0, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int subOK(int x, int y) {
  // compute x - y using two's complement of y
  int diff = (~y + 1) + x;
  // leftmost bit indicates whether x and y have different signs
  int samesign = x ^ y;
  // leftmost bit indicates whether x and (x-y) have different signs
  int diffsign = diff ^ x;
  // if x and x-y will have different signs when x and y have different signs
  // there is overflow
  // eg. x is negative, y is positive, but x-y is positive
  // eg. x is positive, y is negative, but x-y is negative 
  return !((samesign & diffsign) >> 31);
}

